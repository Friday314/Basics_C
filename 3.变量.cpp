/**
 * 3、变量
 *
 * 	格式：
 * 		数据类型 变量名称 = 赋与的值
 * 		int i = 10;
 * 	或者：
 * 		int i;
 * 		i = 10;
 *
 * 		int j,x;
 * 		j = 10;
 * 		x = 11;
 *
 *   	声明一个变量，需要初始化之后才能使用
 */

# include <stdio.h>

int main()
{
	int i = 10;
	int j,x;

	printf("%d\n", i);
	printf("%d\n", j);
	printf("%d\n", x);

	return 0;
}